function setLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    applyLanguage(lang);
}

function applyLanguage(lang) {
    const html = document.documentElement;
    html.lang = lang;
    html.dir = (lang === 'ar') ? 'rtl' : 'ltr';

    // Update Document Title and Meta Description
    if (translations[lang]) {
        if (translations[lang].website_title) {
            document.title = translations[lang].website_title;
        }
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc && translations[lang].website_desc) {
            metaDesc.setAttribute('content', translations[lang].website_desc);
        }
    }

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                const icon = element.querySelector('i');
                if (icon) {
                    const iconHtml = icon.outerHTML;
                    if (lang === 'ar') {
                        element.innerHTML = translations[lang][key] + ' ' + iconHtml;
                    } else {
                        // Flip arrow direction for LTR
                        let updatedIconHtml = iconHtml;
                        if (iconHtml.includes('fa-arrow-right')) {
                            updatedIconHtml = iconHtml.replace('fa-arrow-right', 'fa-arrow-left');
                        } else if (iconHtml.includes('fa-arrow-left')) {
                            updatedIconHtml = iconHtml.replace('fa-arrow-left', 'fa-arrow-right');
                        } else if (iconHtml.includes('fa-chevron-right')) {
                            updatedIconHtml = iconHtml.replace('fa-chevron-right', 'fa-chevron-left');
                        } else if (iconHtml.includes('fa-chevron-left')) {
                            updatedIconHtml = iconHtml.replace('fa-chevron-left', 'fa-chevron-right');
                        } else if (iconHtml.includes('fa-sign-in-alt')) {
                            // In RTL (Arabic) sign-in points left usually, in LTR it points right. 
                            // Since we are moving to LTR here, we check if we need to adjust.
                            // But usually fa-sign-in-alt is LTR friendly.
                        }
                        element.innerHTML = updatedIconHtml + ' ' + translations[lang][key];
                    }
                } else {
                    element.innerHTML = translations[lang][key];
                }
            }
        }
    });

    document.querySelectorAll('img[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.alt = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Update dropdown current text
    const currentLangText = document.getElementById('current-lang-text');
    if (currentLangText) {
        if (lang === 'ar') currentLangText.innerText = 'العربية';
        else if (lang === 'en') currentLangText.innerText = 'English';
        else if (lang === 'fr') currentLangText.innerText = 'Français';
    }

    // Update active class on dropdown buttons
    document.querySelectorAll('.lang-dropdown-content button').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('onclick').includes(`'${lang}'`));
    });

    // Re-render JS-generated accreditation if it exists
    if (typeof window.renderAccreditation === 'function') {
        window.renderAccreditation();
    }

    // Update slider direction if it exists
    if (typeof window.updateSlider === 'function') {
        window.updateSlider();
    }
}

// Helper to get translation by key in current language
window.getT = function (key) {
    const lang = localStorage.getItem('selectedLanguage') || 'ar';
    return (translations[lang] && translations[lang][key]) ? translations[lang][key] : key;
};

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'ar';
    applyLanguage(savedLang);
});
